################################################################
# Imports

from database.config_database import db

################################################################
# Main

class User(db.Model):
    __tablename__ = 'users'

    id = db.Column(db.Integer, primary_key=True)
    name = db.Column(db.String(255))
    email = db.Column(db.String(255))
    password = db.Column(db.String(255))
    cpf = db.Column(db.String(11))